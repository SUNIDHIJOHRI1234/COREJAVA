class Account {  

	private int acc_no;  
	private String name;  
	  
	public int getAcc_no() {  
	    return acc_no;  
	}  
	public void setAcc_no(int acc_no) {  
	    this.acc_no = acc_no;  
	}  
	public String getName() {  
	    return name;  
	}  
	public void setName(String name) {  
	    this.name = name;  
	}
}



	
